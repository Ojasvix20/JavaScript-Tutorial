// While loop
let i = 0;
while (i <= 10) {
  console.log(i);
  i += 2;
}

let myArr = ["flash", "ramesh", "tunnu"];

let arr = 0;
while (arr < myArr.length) {
  console.log(`value is ${myArr[arr]}`);
  arr++;
}

let score = 1;
do {
  console.log(`Score is ${score}`);
  score++;
} while (score <= 10);
